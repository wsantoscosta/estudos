# Faça um algoritmo que leia um número e mostre o seu dobro, triplo e sua raiz quadrada.

numero = int(input("Entre com um número inteiro: "))

print("O número digitado foi : {}".format(numero))
print("O dobro do número é {}".format(numero * 2))
print("O triplo do número é {}".format(numero * 3))
print("A raiz quadrada do número é {}".format(numero ** 2))
